    def _scheduleKey(self, word, it):
        # Rotate:
        word = rot(word)
        out = np.array([0]*4)
        # Sbox:
        for i in range(4):
            out[i] = self._getSboxValue(word[i])
        out[0] = out[0] ^ self._getRconValue(it)
        return out

    def _expandKey(self):
        key = self._key
        size = self._key_size
        expandedSize = self._key_expanded_size
        
        currentSize = 0
        rconIteration = 1
        expandedKey = [0] * expandedSize
        # print len(expandedKey)

        for j in range(size):
            expandedKey[j] = key[j]
        currentSize += size

        while currentSize < expandedSize:
            # temporary storage for the expanded key
            temp = expandedKey[currentSize-4:currentSize]

            # Apply key scheduler to temp:
            if currentSize % size == 0:
                temp = self._scheduleKey(temp, rconIteration)
                rconIteration += 1

            # For big sizes
            if (size == self._key_size) and ((currentSize % size)==16):
                for l in range(4): temp[l] = self._getSboxValue(temp[l])

            # XOR with the blocks of words:
            for m in range(4):
                # print currentSize, " ", currentSize - size, " ", len(expandedKey)
                expandedKey[currentSize] = expandedKey[currentSize - size] ^ temp[m]
                currentSize += 1

        return expandedKey

        
    
